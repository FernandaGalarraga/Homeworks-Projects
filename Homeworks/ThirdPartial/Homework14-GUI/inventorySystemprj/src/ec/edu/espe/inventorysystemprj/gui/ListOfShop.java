    public float displayTotal(ArrayList<ClothingItem> clothesSale) {
        SalesNote sales = new SalesNote();
        Iterator<ClothingItem> it = clothesSale.iterator();
        ClothingItem clothes = new ClothingItem(0, 0);
        float total = 0;
        while (it.hasNext()) {
            clothes = it.next();
            total = total + sales.computeTotal(clothes.getPrice(), clothes.getQuantity());
        }
        return total;
    }


