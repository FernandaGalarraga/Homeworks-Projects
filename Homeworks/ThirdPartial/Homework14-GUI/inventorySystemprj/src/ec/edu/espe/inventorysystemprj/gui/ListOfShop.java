    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed

        ArrayList<ClothingItem> clothes = new ArrayList<ClothingItem>();
        SalesNote sales = new SalesNote();
        FilePrj file = new FilePrj();
        try {
            clothes = file.readClothes("Sales.csv");
        } catch (IOException ex) {
            Logger.getLogger(ListOfShop.class.getName()).log(Level.SEVERE, null, ex);
        }
        sales.setMoneyTotalToPay(this.displayTotal(clothes));
        sales.acumulateMoney();
        try {
            MessageFormat headerFormat = new MessageFormat("Nota de Venta distruibuidora JMG Distribuciones");
            MessageFormat footerFormat = new MessageFormat("Gracias por preferirnos");
            tblProducts.print(JTable.PrintMode.NORMAL, headerFormat, footerFormat);
        } catch (PrinterException e) {
            Logger.getLogger(ListOfShop.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnPrintActionPerformed

