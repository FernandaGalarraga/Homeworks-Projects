    private void btnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseActionPerformed
        if (Validation.isAnyEmpty(txtQuantity)) {
            JOptionPane.showMessageDialog(null, "El campo de cantidad está vacío", "ERROR", WARNING_MESSAGE);
        } else {

            Table tableMethod = new Table();
            FilePrj file = new FilePrj();
            ClothingItem clothing = new ClothingItem(0, 0);
            clothing = tableMethod.getInfoTable(tblProducts);
            try {
                file.changeQuantity("StoreHouse.csv", clothing, -Integer.parseInt(txtQuantity.getText().toString()));
            } catch (IOException ex) {
                Logger.getLogger(SalesNoteFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            clothing.setQuantity(Integer.parseInt(txtQuantity.getText().toString()));
            clothesSale.add(clothing);
            txtQuantity.setText("");

            JOptionPane.showMessageDialog(null, "Se ha agregado el producto correctamente", "Advertencia", WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnChooseActionPerformed

