    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
   
        FilePrj file = new FilePrj();
        Iterator<ClothingItem> it = clothesSale.iterator();
        ClothingItem clothes = new ClothingItem(0, 0);
        file.exists("Sales.csv");
        int i = 0;
        if(!it.hasNext())
        {
            JOptionPane.showMessageDialog(null,"No se ha escogido ninguna prenda","ERROR", WARNING_MESSAGE);
            
        }
        while (it.hasNext()) {
            clothes = it.next();
            file.save("Sales.csv", file.convertsToString(clothes));

            try {
                ListOfShop list = new ListOfShop();
                list.setVisible(true);
                dispose();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null,"No se ha escogido ninguna prenda","ERROR", WARNING_MESSAGE);
            }
        }
          
    }//GEN-LAST:event_btnSaleActionPerformed

