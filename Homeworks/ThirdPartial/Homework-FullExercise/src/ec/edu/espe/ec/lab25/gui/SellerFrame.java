    private void showPVP (ArrayList<Product>product){
       String matriz[][] = new String[product.size()][10];
                for (int i = 0; i < product.size(); i++) {

                    matriz[i][0] = Integer.toString(product.get(i).getId());
                    matriz[i][1] = String.valueOf(product.get(i).getName());
                    matriz[i][2] = Float.toString(product.get(i).getPrice());
                    matriz[i][3] = Float.toString(product.get(i).getPercent());;
                    matriz[i][4] = Float.toString(product.get(i).getPvp());
                    }
     tblProduct.setModel(new javax.swing.table.DefaultTableModel( matriz,new String []{"ID","Nombre","Precio","Porcentaje","PVP"}));               
    }

